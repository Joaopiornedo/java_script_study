// While
let nu = 10

while (nu < 10) {
    console.log('Do While')
    nu++
}
console.log('Fim do Programa')
// no while se o loop ja for falso no caso 10 nao é menor que 10 ele nem chega a executar o programa, finalizando logo de inicio e seguindo o sistema, imprimindo somente o fim do programa no console.


//Do While
let n = 10
do {
    console.log('Do While')
    n++
} while (n < 10)

console.log('Fim do Programa')

//Já no Do while ele roda o programa primeiro e depois verifica a condição do loop e por isso além de fim do programa ele imprime antes um do while tbm.